    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        
        if(txbLogin.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar nombre de usuario");
        }else{
        
        String login=txbLogin.getText();
        String clave=String.valueOf(txbClave.getPassword());
        
        UsuarioDAO usrDAO=new UsuarioDAO();
        UsuarioDTO usr=usrDAO.getUsr(login, clave);
        globalUsr=usr;
        if(usr!=null)
            
        {
          /*  JOptionPane.showMessageDialog(rootPane, usr.getUserName()+" "+usr.getApellUsuario());*/
            this.dispose();
            Principal.setExtendedState(MAXIMIZED_BOTH);
            Principal.setVisible(true);
            
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, "Usuario No Encontrado");
        }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

